public class Calculadora {

    int a,b;  // Declarando a variavel
    public int somar(int a,int b) { //Passando os argumentos necessarios para o metodo
        int resultado = a+b;
        return resultado;

    }
    public float dividir(float a,float b) {
        float resultado = a/b;
        return resultado;
    }

    public float multiplicar(float a,float b) {
        float resultado = a*b;
        return resultado;
    }
    public float subtrair(float a,float b) {
        float resultado = a-b;
        return resultado;
    }

}

